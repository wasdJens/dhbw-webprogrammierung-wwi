const hello = 'Hello';
const world = 'World';

// Both values are a string therefore `+` concats the string values
console.log(hello + " " + world);

const number1 = 5;
const number2 = 5;

// Here both values are a number therefore the "expected" arithmetic operator is done.
console.log(number1 + number2);
console.log(number1 * number2);
console.log(number1 - number2);

const dividend = 20;
const divisor = 4;

console.log(dividend / divisor);